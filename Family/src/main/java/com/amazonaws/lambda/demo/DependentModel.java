package com.amazonaws.lambda.demo;

public class DependentModel {
	
	int menber_id;
	int family_id;
	String name;
	int age;
	String relationship;
	String merital_status;
	
	public int getMenber_id() {
		return menber_id;
	}
	public void setMenber_id(int menber_id) {
		this.menber_id = menber_id;
	}
	public int getFamily_id() {
		return family_id;
	}
	public void setFamily_id(int family_id) {
		this.family_id = family_id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getRelationship() {
		return relationship;
	}
	public void setRelationship(String relationship) {
		this.relationship = relationship;
	}
	public String getMerital_status() {
		return merital_status;
	}
	public void setMerital_status(String merital_status) {
		this.merital_status = merital_status;
	}
	

}
